#max och min värde med "sorted" för att sedan hämta största och minsta värde. Zip används också för att binda samman värdet och månaden som värdet tillhör
def maxminverde(list_of_Choice):
    värde, _, år, _, kolumn_namn, månad = Uppgift_Summa(list_of_Choice)
    sorted_värde = sorted(värde)
    min_värde = sorted_värde[0]
    max_värde = sorted_värde[-1]
    # Flera månader kan ha samma max/min
    max_månad = [m for v, m in zip(värde, månad) if v == max_värde]
    min_månad = [m for v, m in zip(värde, månad) if v == min_värde]
    return (max_värde, max_månad, kolumn_namn, år), (min_värde, min_månad, kolumn_namn, år)


